import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn. linear_model import LinearRegression
from sklearn.metrics import r2_score

url = 'https://raw.githubusercontent.com/kapilpoonia05/Codsoft-tasks/main/Sales%20Prediction/advertising.csv'
df=pd.read_csv(url);

df.head()

df.shape

df.info()

df.isnull().sum()

df.describe()

"""**Separating sales and object individual data**"""

X=df.iloc[:,0:-1]

X

Y=df.iloc[:,-1]

Y

"""Using Train Test Split"""

x_train,x_test,y_train,y_test=train_test_split(X,Y,test_size=0.2,random_state=43)

x_train

y_train

x_test

y_test

x_train=x_train.astype(int)
y_train=y_train.astype(int)
x_test=x_test.astype(int)
y_test=y_test.astype(int)

from sklearn.preprocessing import StandardScaler
Sc=StandardScaler()
x_train_scaled=Sc.fit_transform(x_train)
x_test_scaled=Sc.fit_transform(x_test)

"""We will apply Linear Regression Now"""

lr=LinearRegression()

lr.fit(x_train_scaled,y_train)

y_pred=lr.predict(x_test_scaled)

"""Now we will Check the performance of Linear Regression Model"""

r2_score(y_test,y_pred)

"""Analyzing Data by Scatter Plot"""

plt.scatter(y_test,y_pred,c='g')

